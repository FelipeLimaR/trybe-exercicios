/* 1 - Faça cinco programas, um para cada operação aritmética básica. Seu programa deve ter duas constantes, a e b, definidas no começo com os valores que serão operados. Faça programas para:
*/

const a = 10;
const b = 2;

// Programa 1 Adição (a + b)
let soma = a + b
console.log("O resultado da soma entre", a, "e", b, "é:", soma);


// Programa 2 Subtração (a - b)
let sub = a - b
console.log("O resultado da subtração entre", a, "e", b, "é:", sub);

// Programa 3 Multiplicação (a * b)
let mult = a * b
console.log("O resultado da multiplicação entre", a, "e", b, "é:", mult);

// Programa 4 Divisão (a / b)
let div = a / b
console.log("O resultado da divisão entre", a, "e", b, "é:", div);

// Programa 5 Módulo (a % b)
let mod = a % b
console.log("O resultado da operação de módulo entre", a, "e", b, "é:", mod);